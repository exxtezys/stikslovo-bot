# Build reverse index: word → [emoji1, emoji2, ...]
def build_word_index() -> dict[str, list[str]]:
    """Build a word→emojis lookup for fast search."""
    word_index: dict[str, list[str]] = {}
    for emoji, words in EMOJI_MAP.items():
        for word in words:
            word_index.setdefault(word, []).append(emoji)
    return word_index


